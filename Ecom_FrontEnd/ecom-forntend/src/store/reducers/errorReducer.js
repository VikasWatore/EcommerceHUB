const intitialState = {
  isLoading: false,
  errorMessage: null,
};
export const errorReducer = (state = intitialState, action) => {

  switch (action.type) {
    case "IS_FECTHING":
      return {
        ...state,
        isLoading: true,
        errorMessage: null,
      };

    case "IS_SUCCESS":
      return {
        ...state,
        isLoading: false,
        errorMessage: null,
      };
    case "IS_ERROR":
      return {
        ...state,
        isLoading:false,
        errorMessage:action.payload,
      };

    default:
   return state;
  }


  
};